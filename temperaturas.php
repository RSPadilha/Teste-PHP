/**
* Retorna a temperatura mais proxima de zero.
* Se duas temperaturas com o mesmo valor absouto (uma positiva e outra negativa) serem igualmente proxima a zero, deve ser dada a preferencia para o valor positivo.
* @param array $temperaturas Lista de temperaturas
* @return int A temperatura mais proxima de zero
**/


function menorTemperatura($temperaturas) {
	// Inicializa as variáveis para se ter com o que comparar
	$resultado = $temperaturas[0];
	$menorDistancia = abs($resultado);
	// Começa o loop para checar todos os outros valores
	foreach ($temperaturas as $valor) {
		// Pega o valor absoluto do número já que o mesmo é a distância até o zero
		$distancia = abs($valor);
		// Se a distância do valor atual está no intervalo entre zero e a menor distância registrada
		if ($distancia <= $menorDistancia) {
			// Se for igual, converte para pegar o valor positivo
			if ($distancia == $menorDistancia) {
				$resultado = abs($valor);
			// Senão faz a troca, recebendo o novo menor valor
			} else {
				$resultado = $valor;
				$menorDistancia = $distancia;
			}
		}
	}
	return($resultado);
}


